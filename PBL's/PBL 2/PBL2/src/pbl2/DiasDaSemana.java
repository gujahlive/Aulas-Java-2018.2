public class DiasDaSemana {
    private String diaSemana;
    
    public String getDiaEscolhido() {
        return this.diaSemana;
    }

    public void setDomingo() {
        this.diaSemana = "domingo";
    }

    public void setSegunda() {
        this.diaSemana = "segunda";
    }

    public void setTerca() {
        this.diaSemana = "terca";
    }


    public void setQuarta() {
        this.diaSemana = "quarta";
    }


    public void setQuinta() {
        this.diaSemana = "quinta";
    }



    public void setSexta() {
        this.diaSemana = "sexta";
    }
    
    public void setSabado() {
        this.diaSemana = "sabado";
    }
    
    
}
